package basic;

public class Nov10DataTypes {

	public static void main(String[] args) {
		   

        byte b = 2;
        System.out.println("The byte value is : " + b);

        short s = 127;
        System.out.println("The short value is : " + s);

        int i = 1564123;
        System.out.println("The integer value is : " + i);

        long l = 256145677;
        System.out.println("The long value is : " + l);

        float f = 132.7f;
        System.out.println("The float value is : " + f);

        double d = 165.11248756;
        System.out.println("The double value is : " + d);

        char ch = 'K';
        System.out.println("The character value is : " + ch);

        String S = "Pranav";
        System.out.println("Name is : " + S);

        boolean b1 = true;
        boolean b2 = false;
        System.out.println("The boolean value is : " + b1);
        System.out.println("The boolean value is : " + b2);

	}

}
