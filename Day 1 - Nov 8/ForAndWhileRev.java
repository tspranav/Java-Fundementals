package basic;

public class ForAndWhileRev {

	public static void main(String[] args) {
		//For loop
		  for (int i = 10; i >= 1; i--) {
	            System.out.print(i+" ");
	        }
		  
		  System.out.println();
		
		//While loop
		  int n =10;  
		  while(n>=1)
		  {
			  System.out.print(n+" ");
			  n--;
		  }


	}

}
