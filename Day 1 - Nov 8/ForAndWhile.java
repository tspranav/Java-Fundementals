package basic;

public class ForAndWhile {

	public static void main(String[] args) {
		//For loop
		  for (int i = 1; i <= 10; i++) {
	            System.out.print(i+" ");
	        }
		  
		  System.out.println();
		
		//While loop
		  int n =1;  
		  while(n<=10)
		  {
			  System.out.print(n+" ");
			  n++;
		  }

	}

}
